      Bitcoin (and other crypto-currencies) can be considered an alternative currency not controlled by a central bank. Some economists, such as F. Hayek, consider that these alternative currencies that compete with the official currency make it possible to discipline governments that would be tempted to finance their debt through inflation. In this case, consumers and investors would turn away from the official currency to buy the alternative currency and thus create deflationary pressure on the official currency.
